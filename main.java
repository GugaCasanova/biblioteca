    private void inserirUsuarioJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_inserirUsuarioJButtonActionPerformed
        // TODO add your handling code here:
        Usuario usuario = null;
        if (insereUsuariojComboBox.getSelectedItem() == "Aluno") {
            usuario = new Aluno(jTextField1.getText(), Double.parseDouble(jTextField3.getText()));
        } else if (insereUsuariojComboBox.getSelectedItem() == "Professor") {
            Date data = new java.util.Date();
            // arrumar a data
            usuario = new Professor(jTextField1.getText(), data);
        }
        usuarioArray.add(usuario);
        JOptionPane.showMessageDialog(null, "Usuário inserido com sucesso.");

        usuariosjComboBox.setModel(new DefaultComboBoxModel(usuarioArray.toArray()));

    }//GEN-LAST:event_inserirUsuarioJButtonActionPerformed

