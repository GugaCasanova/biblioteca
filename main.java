    private void inserirMaterialjButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_inserirMaterialjButton4ActionPerformed
        // TODO add your handling code here:
        Obra material;
        if (tipoMaterialjComboBox1.getSelectedItem() == "Áudio") {
            material = new MaterialDigital(tituloMaterialjTextField4.getText(), Integer.parseInt(anoPublicacaojTextField5.getText()),
                    TipoMaterial.AUDIO);
        } else {
            material = new MaterialDigital(tituloMaterialjTextField4.getText(), Integer.parseInt(anoPublicacaojTextField5.getText()),
                    TipoMaterial.VIDEO);
        }

        obraArray.add(material);
        JOptionPane.showMessageDialog(null, "Material Digitado inserido com sucesso.");
        obrasjComboBox.setModel(new DefaultComboBoxModel(obraArray.toArray()));
    }//GEN-LAST:event_inserirMaterialjButton4ActionPerformed

